    "",
    '  "goalMode": string,',
    '  "goalsAvailable": number,',
    '  "gkOptional": boolean',
    "}",
    "",
    "DIAGRAM & SPACING RULES:",
    "- Assume the ATTACKING team always attacks from bottom (y ~ 80) toward top (y ~ 10).",
