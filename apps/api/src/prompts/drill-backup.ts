    '      "Place two mini-goals on one end, 10 yards apart",',
    '      "Place one large goal on opposite end with GK",',
    '      "Split 9 players: 4 attackers, 4 defenders, 1 GK",',
    '      "Position attackers in 2-2 shape",',
    '      "Start with coach pass to attacker"',
    '    ],',
    '    "area": { "lengthYards": 35, "widthYards": 25, "notes": "Attacking third" },',
    '    "rotation": "After 2 goals: attackers→defenders→rest→attackers",',
    '    "restarts": "Coach passes to attacker after goal",',
    '    "scoring": "+1 per goal, +2 for one-touch finish"',
    '  },',
    '  "progressions": ["Add recovering defender", "Require 5 passes minimum"],',
    '  "diagram": { "pitch": {"variant": "HALF"}, "players": [], "goals": [] },',
    '  "numbersOnField": { "attackersOnField": 4, "defendersOnField": 4, "gkForDefend": true, "totalPlayersOnField": 9 }',
    "}",
    "```",
    "",
    "KEY RULES (follow the example above):",
    "1. organization MUST be an OBJECT with setupSteps, area, rotation, restarts, scoring",
    "2. Use 'diagram' NOT 'diagramV1'",
    "3. Use 'progressions' array NOT 'progression'",
    "4. All ages must match input.ageGroup",
    "5. Player counts must be consistent",
    "",
    "═══════════════════════════════════════════════════════════════",
    "CRITICAL: CLARITY + CONSISTENCY RULES",
    "═══════════════════════════════════════════════════════════════",
    "",
    "1) Use ONLY the schema keys provided below. Do NOT output alternate versions like:",
    "   - NO 'diagramV1' (use 'diagram' only)",
    "   - NO 'progression' (use 'progressions' only)",
    "   - NO nested json.json objects or duplicate containers",
    "",
    "2) Age consistency is MANDATORY:",
    "   - Every mention of age MUST equal input.ageGroup",
    "   - NEVER reference other age bands (no 'U12' anywhere if input is 'U10')",
    "   - Check description, organization, coachingPoints, constraints for age mentions",
    "",
    "3) Player counts MUST be consistent everywhere:",
    "   - numbersOnField.totalPlayersOnField MUST be within [input.numbersMin, input.numbersMax]",
    "   - description/organization.setupSteps/diagram must ALL reference the SAME on-field numbers",
    "   - Example: if you say '4v4+GK' in description, diagram must have exactly 4+4+1=9 players",
    "",
    "4) Provide EXACTLY ONE 'diagram' object (NOT 'diagramV1'):",
    "   - Use the canonical diagram schema below",
    "   - Do not duplicate with alternate keys",
    "",
    "5) Provide EXACTLY ONE 'progressions' array (NOT 'progression'):",
    "   - Must be an array of strings",
    "   - 2-4 progressions that build complexity",
    "",
    "6) Space + goals constraints are HARD:",
    "   - Use input.spaceConstraint to choose realistic dimensions",
    "   - Use input.goalsAvailable to set goalMode and scoring",
    "   - If goalsAvailable=0, goalMode='NONE'; if 1, goalMode='LARGE'; if 2+, goalMode='MINI2'",
    "",
    "7) Coach & player level are HARD:",
    "   - Use input.coachLevel to control instruction detail and language complexity",
    "   - Use input.playerLevel to control tactical complexity and decision-making demands",
    "",
    "8) Before outputting JSON, run this MANDATORY checklist:",
    "   ✓ Age mentions = input.ageGroup everywhere?",
    "   ✓ Player counts consistent (description = organization = diagram)?",
    "   ✓ Using 'diagram' (not 'diagramV1')?",
    "   ✓ Using 'progressions' array (not 'progression')?",
    "   ✓ Dimensions realistic for space constraint?",
    "   ✓ Restart/rotation rules crystal clear in organization.rotation?",
    "   ✓ Setup max 3 sentences?",
    "   ✓ Load structure explicit (e.g., '6 x 2:00 / 1:00 rest')?",
