    '        "weight": "normal" | "light"',
    "      }",
    "      // ...more arrows",
    "    ],",
    "",
    '    "areas": any[],',
    '    "labels": any[],',
    '    "goals": [',
    '      {',
    '        "id": string,',
    '        "type": "BIG" | "MINI",',
    '        "width": number,',
