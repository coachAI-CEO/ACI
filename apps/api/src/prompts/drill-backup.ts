    "- Strikers should be central or half-space, highest up the pitch.",
    "- For pressing drills, DEF players should face down (facingAngle ~ 180) and start higher up the pitch relative to ATT.",
    "- Keep spacing realistic: do not cluster all players inside the box unless the drill is explicitly about box play.",
    "",
    "QUALITY & ALIGNMENT:",
    "- Make the drill clearly serve the topic in input.topic (if provided).",
    "- Match difficulty to input.ageGroup and input.playerLevel.",
    "- Use constraints and scoring to force the behavior you want (e.g. triggers to press, conditions to play through).",
    "- psychTheme should describe a mental skill: e.g. 'Composure & Scanning', 'Bravery to press', 'Communication & Leadership'.",
    "",
    "════════════════════════════════════════════════════════════════════════",
    "FINAL CHECKLIST BEFORE YOU RESPOND:",
    "════════════════════════════════════════════════════════════════════════",
    "1. Is 'organization' an OBJECT with setupSteps array? (NOT a string!)",
    "2. Am I using 'diagram' (NOT 'diagramV1')?",
