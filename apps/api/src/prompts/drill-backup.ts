    "",
    '  "numbers": { "min": number, "max": number },',
    '  "durationMin": number,',
    "",
    '  "description": string,  // Max 3 sentences describing what players do',
    "",
    '  "organization": {',
    '    "setupSteps": string[],  // 5-8 bullet steps, EACH STARTS WITH A VERB (Mark, Place, Split, Start, etc.)',
