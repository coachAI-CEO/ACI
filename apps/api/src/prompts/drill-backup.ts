    "- Use formationUsed to determine which roles (GK, CB, FB, WB, DM, CM, AM, W, ST, etc.) are most natural.",
    "- Role labels in the diagram and teamShape MUST align with formationUsed (e.g., 4-3-3 uses ST, LW, RW, not just generic 'forward').",
    "- numbersMin / numbersMax describe typical SESSION numbers, not on-field count for every rep.",
    "- For the drill itself, choose a realistic on-field game (e.g. 4v4+GK, 6v4+GK) that fits the topic and age.",
    "- gkOptional + goalsAvailable tell you whether to include a full GK and how many big goals / mini-goals.",
    "- spaceConstraint tells you how big the area is: FULL, HALF, THIRD, QUARTER.",
    "",
    "LEVEL-BASED CONSTRAINTS:",
    "- playerLevel (BEGINNER/INTERMEDIATE/ADVANCED) determines drill complexity:",
    "  * BEGINNER: Simple 1-2 touch patterns, clear roles, minimal decision-making, short sequences",
