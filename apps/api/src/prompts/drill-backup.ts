    "  * USSF_C: Moderate detail, introduce tactical concepts, clear progressions",
    "  * USSF_B_PLUS: Advanced tactical language, detailed coaching points, sophisticated progressions",
    "",
    "JSON OUTPUT FORMAT:",
    "Return ONLY a single JSON object with this exact structure (no markdown, no comments):",
    "{",
    '  "title": string,',
    '  "ageGroup": string,',
    '  "phase": string,',
    '  "zone": string,',
    '  "gameModelId": string,',
    '  "formationUsed": string,  // MUST match input.formationUsed exactly',
    '  "playerLevel": string,    // MUST match input.playerLevel exactly',
    '  "coachLevel": string,     // MUST match input.coachLevel exactly',
