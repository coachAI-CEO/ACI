    '      "lengthYards": number,',
    '      "widthYards": number,',
    '      "notes": string  // optional: e.g., "Use penalty area", "Half of center circle"',
    '    },',
    '    "rotation": string,  // WHO rotates WHEN, groups, reps (e.g., "Attackers become defenders after 2 goals, defenders rest")',
    '    "restarts": string,  // HOW the ball restarts (e.g., "Coach pass to AM", "GK throws to RB")',
    '    "scoring": string    // HOW to score + bonus points (e.g., "+1 for goal, +2 for one-touch finish")',
    '  },',
    "",
    '  "constraints": string[],',
    '  "progressions": string[],  // 2-4 progressions that build complexity',
    '  "coachingPoints": string[],',
    '  "psychTheme": string,',
    '  "equipment": string[],',
    "",
    '  "loadNotes": {',
    '    "structure": string,  // MUST be explicit like "6 x 2:00 / 1:00 rest (1:0.5)" or "8 x 90s / 90s rest (1:1)"',
    '    "rationale": string',
    '  },',
    "",
    '  "teamShape": {',
    '    "formation": string,',
    '    "attackRoles": [',
    '      { "role": string, "line": "last" | "back" | "middle" | "front", "side": "left" | "right" | "central" },',
    '      ...',
    "    ],",
    '    "defendRoles": [',
    '      { "role": string, "line": "last" | "back" | "middle" | "front", "side": "left" | "right" | "central" },',
    '      ...',
    "    ],",
    '    "notes": string',
    "  },",
    "",
    '  "numbersOnField": {',
    '    "attackersOnField": number,',
    '    "defendersOnField": number,',
    '    "neutralsOnField": number,',
    '    "gkForAttack": boolean,',
    '    "gkForDefend": boolean,',
    '    "totalPlayersOnField": number',
    "  },",
    "",
    '  "roleUsage": {',
    '    "activeAttackRoles": string[],',
    '    "activeDefendRoles": string[],',
    '    "activeNeutralRoles": string[],',
    '    "notes": string',
    "  },",
    "",
    '  "diagram": {  // ⚠️ Use "diagram" NOT "diagramV1"',
    '    "pitch": {',
    '      "variant": "FULL" | "HALF" | "THIRD" | "QUARTER",',
    '      "orientation": "HORIZONTAL",',
    '      "showZones": boolean,',
    '      "zones": {',
    '        "leftWide": boolean,',
    '        "leftHalfSpace": boolean,',
    '        "centralChannel": boolean,',
    '        "rightHalfSpace": boolean,',
    '        "rightWide": boolean',
    "      }",
    "    },",
    "",
    '    "players": [',
    '      {',
    '        "id": string,             // unique ID e.g. "A1", "D2", "N1"',
    '        "number": number,         // shirt number consistent with role (4/5 = CB, 2/3 = FB, 6 = DM, 7/11 = W, 9/10 = ST)',
