    "═══════════════════════════════════════════════════════════════",
    "",
    "EXAMPLE OUTPUT (copy this structure):",
    "```json",
    "{",
    '  "title": "4v4+GK Possession Game in Final Third",',
    '  "ageGroup": "U10",',
